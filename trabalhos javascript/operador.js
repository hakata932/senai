var numero1 = Number(prompt("digite um número: "))
var numero2 = Number(prompt("Digite outro número"))

var somaResultado = numero1 + numero2
alert("A soma dos números é: " + somaResultado)
var subResultado = numero1 - numero2
alert("A subtração dos números é: " + subResultado)
var multResultado = numero1 * numero2
alert("A multiplicação dos números é: " + multResultado)
var expResultado = numero1 ** numero2
alert("A potência dos números é: " + expResultado)
var divResultado = numero1 / numero2
alert("A divisão dos números é: " + divResultado)
var restoResultado = numero1 % numero2
alert("O resto dos números é: " + restoResultado)